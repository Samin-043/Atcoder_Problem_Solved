#include<bits/stdc++.h>
using namespace std;
int main()
{
    char x;
    scanf("%c",&x);
    if(x=='a')
    {
        printf("t");
    }
    else if(x=='t')
    {
        printf("a");
    }
    else if(x=='c')
    {
        printf("g");
    }
    else if(x=='g')
    {
        printf("c");
    }
    printf("\n");
    return 0;
}
